let day = 5;

// Printing days by using switch statement
switch (day) {
  case 1:
    console.log("Monday");
    break;
  case 2:
    console.log("Tuseday");
    break;
  case 3:
    console.log("Wednesday");
    break;
  case 4:
    console.log("Thursday");
    break;
  case 5:
    console.log("Friday");
    break;
  case 6:
    console.log("Saturday");
    break;
  case 7:
    console.log("Sunday");
    break;
  default:
    console.log("Wrong Number");
    break;
}
